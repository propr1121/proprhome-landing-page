// Language Manager
class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('propr-lang') || 'en';
        this.init();
    }

    init() {
        // Set initial language
        this.setLanguage(this.currentLang, false);

        // Set up language buttons
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                const lang = e.target.dataset.lang;
                this.setLanguage(lang);
            });
        });
    }

    setLanguage(lang, animate = true) {
        this.currentLang = lang;
        localStorage.setItem('propr-lang', lang);

        // Update HTML lang attribute
        document.documentElement.lang = lang;

        // Update active button state
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.lang === lang);
        });

        // Translate all elements
        this.translatePage(animate);
    }

    translatePage(animate) {
        const elements = document.querySelectorAll('[data-i18n]');

        elements.forEach(el => {
            const key = el.dataset.i18n;
            const translation = this.getTranslation(key);

            if (translation) {
                if (animate) {
                    el.style.opacity = '0';
                    setTimeout(() => {
                        this.updateElement(el, translation);
                        el.style.opacity = '1';
                    }, 150);
                } else {
                    this.updateElement(el, translation);
                }
            }
        });

        // Handle placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.dataset.i18nPlaceholder;
            const translation = this.getTranslation(key);
            if (translation) {
                el.placeholder = translation;
            }
        });

        // Handle aria-labels
        document.querySelectorAll('[data-i18n-aria]').forEach(el => {
            const key = el.dataset.i18nAria;
            const translation = this.getTranslation(key);
            if (translation) {
                el.setAttribute('aria-label', translation);
            }
        });
    }

    updateElement(el, translation) {
        // Check if element has child elements to preserve (like icons)
        const hasIconChild = el.querySelector('img, svg');

        if (hasIconChild && el.childNodes.length > 1) {
            // Find and update only text nodes
            el.childNodes.forEach(node => {
                if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
                    node.textContent = translation;
                }
            });
        } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.value = translation;
        } else {
            el.textContent = translation;
        }
    }

    getTranslation(key) {
        return translations[this.currentLang]?.[key] || translations['en']?.[key] || key;
    }

    getCurrentLanguage() {
        return this.currentLang;
    }
}

